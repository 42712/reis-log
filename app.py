def api_admin_import():
    """
    Importa CSV em massa.
    Body: { "password":"...", "csv":"cep,rota\n74080-010,119\n..." }
    """
    data = request.get_json(force=True)
    password = os.environ.get("ADMIN_PASSWORD", "rota202601")
    if data.get("password") != password:
        return jsonify({"ok": False, "error": "Senha incorreta"}), 403

    extras = load_extras()
    added = skipped = 0
    for line in data.get("csv", "").splitlines():
        parts = re.split(r"[;,\t]", line.strip())
        if len(parts) < 2:
            skipped += 1
            continue
        cep = normalize_cep(parts[0])
        rota = parts[1].strip().upper()
        if cep and is_valid_cep(cep) and rota:
            extras[cep] = rota
            added += 1
        else:
            skipped += 1

    save_extras(extras)
    return jsonify({"ok": True, "added": added, "skipped": skipped})

