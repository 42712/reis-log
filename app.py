# HELPERS CEP
# ─────────────────────────────────────────────────────────────────────────────
def normalize_cep(raw: str) -> str | None:
    digits = re.sub(r"\D", "", str(raw))
    if len(digits) >= 8:
        return f"{digits[:5]}-{digits[5:8]}"
    return None

def is_valid_cep(cep: str) -> bool:
    if not cep or not re.match(r"^\d{5}-\d{3}$", cep):
        return False
    digits = cep.replace("-", "")
    if len(set(digits)) == 1:
        return False
    n = int(digits)
    return 1_000_000 <= n <= 99_999_999

def db_lookup(raw: str) -> dict | None:
    db = get_full_db()
    cep = normalize_cep(raw)

    # ── 1. Busca exata ──
    if cep and cep in db:
        return {"cep": cep, "rota": db[cep]}

    # ── 2. Busca exata por dígitos (sem traço) ──
    digits = re.sub(r"\D", "", str(raw))[:8]
    for k, v in db.items():
        if k.replace("-", "") == digits:
            return {"cep": k, "rota": v}

    # ── 3. Fallback por prefixo (5 primeiros dígitos) ──
    # CEPs do mesmo bairro/logradouro geralmente compartilham o prefixo
    # e pertencem à mesma rota. Evita "não encontrado" por sufixo incomum.
    if cep:
        prefix = cep[:5]          # ex: "74340"
        candidates = {k: v for k, v in db.items() if k.startswith(prefix + "-")}
        if candidates:
            # Retorna a rota mais comum dentro desse prefixo
            from collections import Counter
            rota = Counter(candidates.values()).most_common(1)[0][0]
            app.logger.info(f"CEP {cep} não encontrado exato; usando prefixo {prefix} → rota {rota}")
            return {"cep": cep, "rota": rota, "fallback": True}

    return None

