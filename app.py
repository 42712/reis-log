GEMINI_PROMPT = """Você é um extrator de CEP brasileiro especializado.

Analise a imagem e encontre o CEP (código postal do Brasil).

O CEP pode aparecer:
- Impresso em etiqueta de entrega (NNNNN-NNN ou NNNNNNNN)
- Escrito à mão com caneta, marcador ou pincel
- Em etiqueta de código de barras
- Em tela de computador ou tablet

Regras:
- CEP tem SEMPRE 8 dígitos
- Corrija: O→0, l/I→1, S→5, B→8, Z→2, G→6
- Se houver múltiplos números de 8 dígitos, escolha o que parece CEP

Responda SOMENTE JSON sem markdown:
{"cep": "NNNNN-NNN"}

Se não encontrar CEP claro:
{"cep": null}"""

def ask_gemini(image_b64: str, mime: str = "image/jpeg") -> tuple:
    """
    Retorna (cep_str | None, erro_str | None).
    - (cep, None)   → CEP extraído com sucesso
    - (None, None)  → API ok mas Gemini não achou CEP na imagem
    - (None, msg)   → Falha na chamada à API (quota, chave inválida, etc.)
    """
    # Verifica chave antes de fazer a chamada
    try:
        urls = [get_gemini_url(), get_gemini_url_fallback()]
    except ValueError as e:
        return None, str(e)

    payload = {
        "contents": [{"parts": [
            {"text": GEMINI_PROMPT},
            {"inline_data": {"mime_type": mime, "data": image_b64}}
        ]}],
        "generationConfig": {
            "maxOutputTokens": 50,
            "temperature": 0,
            "responseMimeType": "application/json"
        }
    }

    last_error = None
    for url in urls:
        try:
            resp = requests.post(url, json=payload, timeout=25)

            # Erros HTTP com mensagem legível
            if resp.status_code == 400:
                last_error = "Imagem inválida ou muito pequena (erro 400)"
                continue
            if resp.status_code == 403:
                last_error = "Chave Gemini inválida ou sem permissão (erro 403)"
                continue
            if resp.status_code == 429:
                last_error = "Cota da API Gemini esgotada (erro 429) — aguarde ou troque a chave"
                continue
            if resp.status_code == 404:
                app.logger.warning(f"Modelo não encontrado ({url}), tentando fallback...")
                last_error = "Modelo Gemini não encontrado"
                continue

            resp.raise_for_status()
            data = resp.json()

            # Checa se Gemini retornou candidatos
            candidates = data.get("candidates", [])
            if not candidates:
                block = data.get("promptFeedback", {}).get("blockReason", "")
                last_error = f"Gemini bloqueou a imagem: {block}" if block else "Resposta vazia do Gemini"
                continue

            raw = candidates[0]["content"]["parts"][0]["text"].strip()
            raw = re.sub(r"```json?|```", "", raw).strip()
            parsed = json.loads(raw)
            cep_val = parsed.get("cep")
            # None significa que Gemini não encontrou CEP (mas a API funcionou)
            return cep_val, None

        except requests.exceptions.Timeout:
            last_error = "Timeout ao chamar Gemini (>25s)"
            app.logger.warning(f"Gemini timeout ({url})")
            continue
        except Exception as e:
            last_error = f"Erro interno: {type(e).__name__}"
            app.logger.warning(f"Gemini error ({url}): {e}")
            continue

    return None, last_error or "Falha ao conectar ao Gemini"

