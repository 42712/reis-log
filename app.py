def get_full_db() -> dict:
    """Retorna DB completo: base + extras."""
    db = dict(CEP_DB)
    db.update(load_extras())
    return db

def normalize_cep(raw: str) -> str | None:
    """Normaliza para formato NNNNN-NNN."""
    digits = re.sub(r"\D", "", str(raw))
    if len(digits) >= 8:
        return f"{digits[:5]}-{digits[5:8]}"
    return None

def is_valid_cep(cep: str) -> bool:
    """Valida CEP brasileiro."""
    if not cep or not re.match(r"^\d{5}-\d{3}$", cep):
        return False
    digits = cep.replace("-", "")
    if len(set(digits)) == 1:  # todos iguais ex: 00000-000
        return False
    n = int(digits)
    return 1_000_000 <= n <= 99_999_999

def db_lookup(raw: str) -> dict | None:
    """Busca CEP no banco de dados."""
    db = get_full_db()
    cep = normalize_cep(raw)
    if cep and cep in db:
        return {"cep": cep, "rota": db[cep]}
    # Busca fuzzy por dígitos
    digits = re.sub(r"\D", "", str(raw))[:8]
    for k, v in db.items():
        if k.replace("-", "") == digits:
            return {"cep": k, "rota": v}
    return None

