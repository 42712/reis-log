@app.route("/api/scan", methods=["POST"])
def api_scan():
    """
    Recebe imagem base64, chama Gemini, busca no DB.
    Body JSON: { "image": "<base64>", "mime": "image/jpeg" }
    Resposta: { "ok": true, "cep": "74350-200", "rota": "100" }
           ou { "ok": false, "error": "CEP não encontrado" }
    """
    data = request.get_json(force=True)
    image_b64 = data.get("image", "")
    mime = data.get("mime", "image/jpeg")

    if not image_b64:
        return jsonify({"ok": False, "error": "Imagem não enviada"}), 400

    # Gemini extrai o CEP
    cep_raw = ask_gemini(image_b64, mime)

    if not cep_raw:
        return jsonify({"ok": False, "error": "Nenhum CEP detectado na imagem"})

    cep = normalize_cep(cep_raw)

    if not cep or not is_valid_cep(cep):
        return jsonify({"ok": False, "error": f"CEP inválido: {cep_raw}"})

    # Busca no banco
    result = db_lookup(cep)
    if result:
        return jsonify({"ok": True, "cep": result["cep"], "rota": result["rota"]})
    else:
        return jsonify({"ok": True, "cep": cep, "rota": None,
                        "error": "CEP não encontrado na base"})

