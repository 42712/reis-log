@app.route("/api/scan", methods=["POST"])
def api_scan():
    """
    Aceita DOIS formatos de entrada:

    1. Multipart form (câmera nativa do celular):
       POST /api/scan   Content-Type: multipart/form-data
       Campo: photo = arquivo de imagem

    2. JSON base64 (chamadas programáticas):
       POST /api/scan   Content-Type: application/json
       Body: { "image": "<base64>", "mime": "image/jpeg" }

    Resposta JSON:
       { "ok": true,  "cep": "74350-200", "rota": "100" }
       { "ok": false, "error": "mensagem" }
    """
    image_b64 = ""
    mime      = "image/jpeg"

    # ── Tenta multipart primeiro (câmera nativa) ──
    if request.content_type and "multipart" in request.content_type:
        photo = request.files.get("photo")
        if not photo:
            return jsonify({"ok": False, "error": "Arquivo não enviado"}), 400
        mime = photo.mimetype or "image/jpeg"
        image_b64 = base64.b64encode(photo.read()).decode("utf-8")

    # ── Fallback: JSON base64 ──
    else:
        try:
            data = request.get_json(force=True) or {}
        except Exception:
            data = {}
        image_b64 = data.get("image", "")
        mime      = data.get("mime", "image/jpeg")

    if not image_b64:
        return jsonify({"ok": False, "error": "Imagem não enviada"}), 400

    # ── Gemini extrai o CEP ──
    cep_raw, gemini_error = ask_gemini(image_b64, mime)

    if cep_raw is None:
        if gemini_error:
            # Erro real de API — retorna a mensagem detalhada
            app.logger.error(f"Gemini API error: {gemini_error}")
            return jsonify({"ok": False, "error": gemini_error})
        else:
            # API funcionou mas nenhum CEP encontrado na imagem
            return jsonify({"ok": False, "error": "Nenhum CEP detectado na imagem"})

    cep = normalize_cep(cep_raw)

    if not cep or not is_valid_cep(cep):
        return jsonify({"ok": False, "error": f"CEP inválido detectado: {cep_raw}"})

    # ── Busca no banco (dict em memória — 0ms) ──
    result = db_lookup(cep)
    if result:
        resp = {"ok": True, "cep": result["cep"], "rota": result["rota"]}
        if result.get("fallback"):
            resp["aviso"] = "Rota estimada por prefixo — CEP exato não cadastrado"
        return jsonify(resp)
    else:
        return jsonify({"ok": True, "cep": cep, "rota": None,
                        "error": "CEP não encontrado na base"})

